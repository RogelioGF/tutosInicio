public class Ejercicio3 {
	//	Desarrollar un programa que muestre la tabla de multiplicar del 5 (del 5 al 50)
	public static void main(String[] args) {
		int multi;
		
		for (int i = 1; i<=50; i++) {
			multi =5*i;
			System.out.println("5 x " + i + " = " + multi);
		}

	}

}
